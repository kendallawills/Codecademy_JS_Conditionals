// If Statement
let sale = true;

if (sale) {
    console.log('Time to buy!');
}

// If...Else Statements
let sales = true;
sales = false;

if (sales) {
    console.log('Time to buy!');
} else {
    console.log('Time to wait for a sale.');
}

//Comparison Operators
let hungerLevel = 7;

if (hungerLevel > 7) {
    console.log('Time to eat!');
} else {
    console.log('We can eat later!');
}